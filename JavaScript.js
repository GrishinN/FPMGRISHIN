    function removePhotoPost(value) {

        for (var i = 0; i < photoPosts.length; i++) {
            if (photoPosts[i].id === value){
                photoPosts.splice(i,1);
                return 1;
            }
        }

        return -1;
    }
    function getPhotoPost(value) { // 1й выриант
        for (var i = 0; i < photoPosts.length; i++) {
            if (photoPosts[i].id === value){

                return photoPosts[i];
            }
        }

        return -1;
    }

    function getPhotoPost(value) { // 2й вариант
        a = photoPosts.findIndex(item => item.id === value);
        if(a !== -1 )
        {
            return photoPosts[a];
        }else{
            return false;
        }

    }

    function validatePhotoPost(photoPosts) {
        if((typeof photoPosts.id !== "string") || photoPosts.id == '') {
            return -1;
        }
        if((typeof photoPosts.descriprion !== "string") || photoPosts.descriprion == '' || photoPosts.descriprion.length > 200) {
            return -1;
        }
        if((typeof photoPosts.createdAt !== "object" || photoPosts.createdAt == '') ) {
            return -1;
        }

        if((typeof photoPosts.author !== "string") || photoPosts.author == '') {
            return -1;
        }
        if((typeof photoPosts.photoLink !== "string") || photoPosts.photoLink == '') {
            return -1;
        }
        return true;
    }

    function addPhotoPost(photoPosts1) {
        if(validatePhotoPost(photoPosts1)){
            if(photoPosts.push(photoPosts1)) {
                return true;
            }
        }
        return false;
    }

    function  editPhotoPost(id , photoPosts1 ) {
        var item = photoPosts.findIndex(item => item.id === id);
        if(validatePhotoPost(photoPosts[item])){
            for(var key in photoPosts1){
                if(key == "descriprion"){
                    photoPosts[item].descriprion = photoPosts1[key];
                }
                if(key == "createdAt"){
                    photoPosts[item].createdAt = photoPosts1[key];
                }
                if(key == "photoLink"){
                    photoPosts[item].photoLink = photoPosts1[key];
                }
            }
        }
    }

    function compareDate(photoPostsA,photoPostsB) {
        return photoPostsA.createdAt - photoPostsB.createdAt;
    }
    function getPhotoPosts(skip , top , filterConfig) {
        var newMass1;
        var newMass = photoPosts.concat();
        if(filterConfig === undefined){
            newMass.sort(compareDate);
            return newMass.slice(skip,top);
        }else{
            for (var i = 0; i < photoPosts.length; i++) {
                if (photoPosts[i].author == filterConfig)
                {
                    newMass1.push(photoPosts[i])
                    newMass1.sort(compareDate);
                    return newMass.slice(skip, top);
                }
            }
        }

    }






